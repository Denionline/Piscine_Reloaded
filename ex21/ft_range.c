#include <stdlib.h>

int	*ft_range(int min, int max)
{
	unsigned int	i;
	unsigned int	size;
	int				*range;

	if (min >= max)
		return (NULL);
	size = (max - min);
	range = (int *)malloc((size) * sizeof(int));
	if (!range)
		return (NULL);
	i = 0;
	while (i < size)
	{
		range[i] = min + i;
		i++;
	}
	return (range);
}
